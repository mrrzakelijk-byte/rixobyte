const html = document.documentElement;
const cursorLight = document.querySelector(".cursor-light");
const languageButtons = document.querySelectorAll(".language-button");
const translatableElements = document.querySelectorAll("[data-i18n]");
const placeholderElements = document.querySelectorAll("[data-i18n-placeholder]");
const menuButton = document.querySelector(".menu-button");
const navLinks = document.querySelectorAll(".nav-links a");
const revealElements = document.querySelectorAll(".reveal");
const tiltCards = document.querySelectorAll(".tilt-card");
const magneticItems = document.querySelectorAll(".magnetic");
const quoteForm = document.querySelector("#quoteForm");

function getLanguage() {
  const savedLanguage = localStorage.getItem("siteLanguage");
  return savedLanguage === "en" ? "en" : "nl";
}

function setLanguage(language) {
  const dictionary = translations[language] || translations.nl;
  html.lang = language;
  localStorage.setItem("siteLanguage", language);

  translatableElements.forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) element.textContent = dictionary[key];
  });

  placeholderElements.forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (dictionary[key]) element.placeholder = dictionary[key];
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === language);
  });
}

function updateCursorLight(event) {
  if (!cursorLight) return;
  cursorLight.style.left = `${event.clientX}px`;
  cursorLight.style.top = `${event.clientY}px`;
}

function toggleMenu() {
  const isOpen = document.body.classList.toggle("menu-open");
  if (menuButton) menuButton.setAttribute("aria-expanded", String(isOpen));
}

function closeMenu() {
  document.body.classList.remove("menu-open");
  if (menuButton) menuButton.setAttribute("aria-expanded", "false");
}

function setupRevealAnimations() {
  if (!revealElements.length) return;

  if (!("IntersectionObserver" in window)) {
    revealElements.forEach((element) => element.classList.add("visible"));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.14 }
  );

  revealElements.forEach((element) => observer.observe(element));
}

function setupTiltCards() {
  tiltCards.forEach((card) => {
    card.addEventListener("mousemove", (event) => {
      const rect = card.getBoundingClientRect();
      const x = event.clientX - rect.left;
      const y = event.clientY - rect.top;
      const rotateX = ((y / rect.height) - 0.5) * -5;
      const rotateY = ((x / rect.width) - 0.5) * 5;
      card.style.transform = `perspective(900px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateY(-2px)`;
    });

    card.addEventListener("mouseleave", () => {
      card.style.transform = "";
    });
  });
}

function setupMagneticItems() {
  magneticItems.forEach((item) => {
    item.addEventListener("mousemove", (event) => {
      const rect = item.getBoundingClientRect();
      const x = event.clientX - rect.left - rect.width / 2;
      const y = event.clientY - rect.top - rect.height / 2;
      item.style.transform = `translate(${x * 0.1}px, ${y * 0.12}px)`;
    });

    item.addEventListener("mouseleave", () => {
      item.style.transform = "";
    });
  });
}

function updateActiveNav() {
  const currentPath = window.location.pathname.split("/").pop() || "index.html";
  navLinks.forEach((link) => {
    const href = link.getAttribute("href") || "";
    if (currentPath === "info.html") {
      link.classList.toggle("active", href.includes("info.html"));
    } else if (href === "#top" || href === "index.html#top") {
      link.classList.add("active");
    }
  });
}

async function submitQuoteForm(event) {
  event.preventDefault();

  const language = getLanguage();
  const dictionary = translations[language] || translations.nl;

  const name = document.querySelector("#name")?.value.trim() || "";
  const email = document.querySelector("#email")?.value.trim() || "";
  const message = document.querySelector("#message")?.value.trim() || "";
  const submitButton = quoteForm?.querySelector(".form-submit");
  const formNote = quoteForm?.querySelector(".form-note");

  if (!name || !email || !message) {
    alert(dictionary.errorRequired);
    return;
  }

  const formData = new FormData(quoteForm);
  formData.append("from_name", name);
  formData.append("replyto", email);

  const originalButtonText = submitButton?.textContent;
  if (submitButton) {
    submitButton.disabled = true;
    submitButton.textContent = language === "en" ? "Sending..." : "Versturen...";
  }
  if (formNote) formNote.textContent = language === "en" ? "Sending your request..." : "Je aanvraag wordt verstuurd...";

  try {
    const response = await fetch("https://api.web3forms.com/submit", {
      method: "POST",
      body: formData
    });
    const result = await response.json();

    if (response.ok && result.success) {
      if (formNote) formNote.textContent = dictionary.successMessage;
      quoteForm.reset();
    } else {
      throw new Error(result.message || "Form submission failed");
    }
  } catch (error) {
    if (formNote) formNote.textContent = dictionary.errorMessage;
  } finally {
    if (submitButton) {
      submitButton.disabled = false;
      submitButton.textContent = originalButtonText || dictionary.submitButton;
    }
  }
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

if (menuButton) menuButton.addEventListener("click", toggleMenu);
navLinks.forEach((link) => link.addEventListener("click", closeMenu));
window.addEventListener("mousemove", updateCursorLight);
if (quoteForm) quoteForm.addEventListener("submit", submitQuoteForm);

setLanguage(getLanguage());
setupRevealAnimations();
setupTiltCards();
setupMagneticItems();
updateActiveNav();
